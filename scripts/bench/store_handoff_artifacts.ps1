<#
.SYNOPSIS
Copy benchmark evidence artifacts into a release handoff folder.

.DESCRIPTION
Takes benchmark artifacts generated by local CI for one tier and stores them
under docs/plans/handoffs/<stamp>/<tier>/ with checksum metadata.
#>
param(
    [Parameter(Mandatory = $true)]
    [ValidateSet("desktop_mid", "laptop_integrated")]
    [string]$Tier,

    [string]$SourceRoot = "target/bench",
    [string]$HandoffRoot = "docs/plans/handoffs",
    [string]$Stamp = ""
)

$ErrorActionPreference = "Stop"

if ([string]::IsNullOrWhiteSpace($Stamp)) {
    $Stamp = (Get-Date).ToUniversalTime().ToString("yyyyMMdd-HHmmss")
}

$sourceDir = Join-Path $SourceRoot $Tier
$required = @("benchmark_report.md", "benchmark_metrics.ini")
foreach ($file in $required) {
    $path = Join-Path $sourceDir $file
    if (-not (Test-Path -LiteralPath $path -PathType Leaf)) {
        throw "missing benchmark artifact for tier '$Tier': $path"
    }
}

$destDir = Join-Path (Join-Path $HandoffRoot $Stamp) $Tier
New-Item -ItemType Directory -Path $destDir -Force | Out-Null

$rows = @()
foreach ($file in $required) {
    $src = Join-Path $sourceDir $file
    $dst = Join-Path $destDir $file
    Copy-Item -LiteralPath $src -Destination $dst -Force
    $item = Get-Item -LiteralPath $dst
    $hash = (Get-FileHash -Algorithm SHA256 -LiteralPath $dst).Hash.ToLowerInvariant()
    $rows += [PSCustomObject]@{
        Tier = $Tier
        File = $file
        SizeBytes = $item.Length
        Sha256 = $hash
    }
}

$manifestPath = Join-Path $destDir "artifact_manifest.txt"
$manifestLines = @(
    "stamp=$Stamp",
    "tier=$Tier",
    "source_dir=$sourceDir",
    "copied_utc=$((Get-Date).ToUniversalTime().ToString('u'))",
    ""
)
$manifestLines += ($rows | ForEach-Object {
    "file=$($_.File) size_bytes=$($_.SizeBytes) sha256=$($_.Sha256)"
})
$manifestLines | Set-Content -LiteralPath $manifestPath -Encoding UTF8

Write-Host "stored handoff artifacts for tier '$Tier' at $destDir"
